for i in range(1, 6):  # Bucle 'for' que itera desde 1 hasta 5
    while i <= 4:      # Bucle 'while' que se ejecuta mientras 'i' sea menor o igual a 4
        i += 1         # Incrementa el valor de 'i' en 1
        print(i)       # Imprime el valor actual de 'i'
    break              # Rompe el ciclo 'for' inmediatamente después de la primera iteración 